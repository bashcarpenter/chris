#!/bin/bash 
export PATH=$PATH:/usr/local/sbin:/usr/local/bin:/sbin:/bin:/usr/sbin:/usr/bin:/root/bin

time_interval=15;

NOCOLOR='\033[0m';
RED='\033[0;31m';
GREEN='\033[0;32m';
ORANGE='\033[0;33m';
BLUE='\033[0;34m';
PURPLE='\033[0;35m';
CYAN='\033[0;36m';
LIGHTGRAY='\033[0;37m';
DARKGRAY='\033[1;30m';
LIGHTRED='\033[1;31m';
LIGHTGREEN='\033[1;32m';
YELLOW='\033[1;33m';
LIGHTBLUE='\033[1;34m';
LIGHTPURPLE='\033[1;35m';
LIGHTCYAN='\033[1;36m';
WHITE='\033[1;37m'; 

current_time=$(date +%s);
max_time=$(( $current_time + $time_interval ));

count=1;
while [ $count -eq "1" ]
do
	if [[ $current_time -ge $max_time ]]
	then
echo -e "${YELLOW}................................................................
................................................................
................................................................
................................................................
................................................................
................................................................
!!!!!!!!!!!!!!!!!!SEARCH AGAIN NOW !!!!!!!!!!!!!!!!!!!!!!!!!!!!!
................................................................
................................................................
................................................................
................................................................
................................................................
................................................................";
		current_time=$(date +%s);
		max_time=$(( $current_time + $time_interval ));
		sleep 1;
	else
		current_time=$(date +%s);
		remaining_time=$(( $max_time - $current_time ));
		echo -e "${WHITE}$remaining_time seconds left for next search${NOCOLOR}";
		sleep 1;
	fi
done
